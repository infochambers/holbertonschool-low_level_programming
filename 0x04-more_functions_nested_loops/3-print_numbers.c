#include "holberton.h"

/**
 * print_numbers --  a function that prints the
 * numbers, from 0 to 9, followed by a new line.
 *
 *
 *
 * Return: Always 0 (Success)
 *
 */

void print_numbers(void)
{

	int srebmun;

	for (srebmun = 0 ; srebmun < 10 ; srebmun++)
	{
		_putchar(srebmun + '0');
	}

	_putchar('\n');


}
