#include "holberton.h"

/**
 * more_numbers --  a function that prints 10 times the
 * numbers, from 0 to 14, followed by a new line.
 *
 *
 *
 * Return: Always 0 (Success)
 *
 */

void more_numbers(void)
{

	int srebmun;

	for (srebmun = 0 ; srebmun < 15 ; srebmun++)
	{
		_putchar(srebmun + '0');
	}

	_putchar('\n');


}
